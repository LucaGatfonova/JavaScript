function add(a, b) {
    return ('Сложение: ' + (a + b));
}

function subtract(a, b) {
    return ('Вычитание: ' + (a - b));
}

function div (a, b) {
    return ('Деление: ' + a / b);
}

function mult (a, b) {
    return ('Умножение: ' + a * b);
}
console.log(add(10, 3))
console.log(subtract(10, 3))
console.log(div(10, 3))
console.log(mult(10, 3))